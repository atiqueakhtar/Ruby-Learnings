# in a hash we can store data in a key-value pair
# only unique keys allowed
states = {
    "Jharkhand" => "JHR",
    "Assam" => "ASM",
    "Hyderabad" => "HYD"
}
puts(states)
# {"Jharkhand"=>"JHR", "Assam"=>"ASM", "Hyderabad"=>"HYD"}
puts(states["Assam"])
# ASM


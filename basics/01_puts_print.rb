# puts takes us to the new line after printing whereas print does not.
puts "Hello Atique!!"
print "Hello World!"
puts ""
print "Atique "
print "is cool"
puts ""

# Output :
# Hello Atique!!
# Hello World!
# Atique is cool

# triangle with puts
puts "   /|"
puts "  / |"
puts " /  |"
puts "/___|"

# Output :
#    /|
#   / |
#  /  |
# /___|